export interface ShippingAddress {
  fullName: string;
  email: string;
  phone: string;
  street: string;
  apartment?: string;
  city: string;
  state: string;
  zip: string;
  country: string;
}

export interface OrderItem {
  productId: string;
  name: string;
  brand: string;
  size: number;
  quantity: number;
  price: number;
  imageUrl: string;
}

export type OrderStatus = 'pending' | 'processing' | 'shipped' | 'delivered' | 'cancelled';

export type PaymentStatus = 'pending' | 'paid' | 'failed';

export interface Order {
  orderId: string;
  userId: string;
  userEmail: string;
  items: OrderItem[];
  shippingAddress: ShippingAddress;
  orderDate: string;
  status: OrderStatus;
  subtotal: number;
  shipping: number;
  tax: number;
  total: number;
  estimatedDelivery?: string;
  promoCode?: string;
  discount?: number;
  // Payment fields (Stripe integration)
  paymentStatus?: PaymentStatus;
  stripeSessionId?: string;
  paymentMethod?: string;
  paymentDate?: string;
}

